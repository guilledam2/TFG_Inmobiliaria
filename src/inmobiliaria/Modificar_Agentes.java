    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id_nuevo = Integer.parseInt(this.jTextField1.getText());
        String nombre_nuevo = this.nombrecompleto_textfield.getText();
        String correo_nuevo = this.correo_textfield.getText();
        String usuario_nuevo = this.usuario_textfield.getText();
        String contrasena_nuevo = this.contrasena_textfield.getText();
        
        if(!validarCorreoElectronico(correo_nuevo)){
            JOptionPane.showMessageDialog(this, "Formato de correo electrónico no válido", "Error", JOptionPane.ERROR_MESSAGE);
            this.correo_textfield.setText("");
        }else if(!(contrasena_nuevo.length() >= 7 && contrasena_nuevo.length() <= 15)){
            JOptionPane.showMessageDialog(this, "La longitud de la contraseña debe estar entre 7 y 15 caracteres", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            this.modificar_datos(id_nuevo, nombre_nuevo, correo_nuevo, usuario_nuevo, contrasena_nuevo);
        }

        

    }//GEN-LAST:event_jButton2ActionPerformed

