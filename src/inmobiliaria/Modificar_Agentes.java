    public void modificar_datos(int id, String nombre_nuevo, String correo_nuevo, String usuario_nuevo, String contrasena_nuevo) {

        Connection con = activar();
        int opcion = JOptionPane.showConfirmDialog(null, "¿Desea realizar los cambios en la base de datos?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            try {

                Statement stmt = con.createStatement();

                String actualizado = "UPDATE AgentesInmobiliarios SET nombre = '" + nombre_nuevo + "', correo = '" + correo_nuevo + "', nombre_usuario = '" + usuario_nuevo + "', contrasena = '" + contrasena_nuevo + "' WHERE id_agente = " + id;

                stmt.executeUpdate(actualizado);
                JOptionPane.showMessageDialog(null, "Ha sido modificado", "Éxito", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

